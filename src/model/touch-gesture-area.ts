/**
 * On-screen device touch area
 */
export enum TouchGestureArea {
    /**
     * Left of screen
     */
    Left   = 'left',
    /**
     * Right of screen
     */
    Right  = 'right',
    /**
     * Top of screen
     */
    Top    = 'top',
    /**
     * Bottom of screen
     */
    Bottom = 'bottom',
}
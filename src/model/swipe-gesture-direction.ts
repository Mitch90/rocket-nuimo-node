/**
 * On-screen swipe gesture direction
 */
export enum SwipeGestureDirection {
    /**
     * Right to left on-screen swipe
     */
    Left  = 'left',
    /**
     * Left to right on-screen swipe
     */
    Right = 'right',
    /**
     * Bottom to top on-screen swipe
     */
    Up    = 'up',
    /**
     * Top to bottom on-screen swipe
     */
    Down  = 'down',
}

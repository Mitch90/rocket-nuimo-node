/**
 * Direction for fly gestures
 */
export enum HoverGesture {
    /**
     * Swipe right to left above the screen
     */
    Left  = 'left',
    /**
     * Swipe left to right above the screen
     */
    Right = 'right',
    /**
     * Hover towards the screen
     */
    In    = 'in',
    /**
     * Hover away from the screen
     */
    Out   = 'out',
}